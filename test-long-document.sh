机器学习是人工智能的核心分支，它使计算机能够从数据中学习而无需明确编程。机器学习算法可以分为三大类：

1. 监督学习：使用标记数据训练模型，如分类和回归问题。
2. 无监督学习：从未标记数据中发现模式，如聚类和降维。
3. 强化学习：通过与环境交互学习最优策略。

深度学习是机器学习的一个子领域，它使用多层神经网络来学习数据的层次化表示。深度学习在图像识别、自然语言处理、语音识别等领域取得了突破性进展。

重要的深度学习架构包括：
- 卷积神经网络（CNN）：主要用于图像处理
- 循环神经网络（RNN）：用于序列数据处理
- Transformer：现代自然语言处理的基础
- 生成对抗网络（GAN）：用于生成新内容

第四章：自然语言处理

自然语言处理（NLP）是人工智能的重要应用领域，目标是让计算机理解和生成人类语言。

NLP的关键技术包括：
- 词嵌入（Word Embeddings）：将词语映射到向量空间
- 序列到序列模型：用于机器翻译等任务
- 注意力机制：能够关注输入的重要部分
- 预训练语言模型：如BERT、GPT等

近年来，大型语言模型（LLM）的发展极大地推动了NLP领域的进步。GPT-3、ChatGPT等模型展示了惊人的语言理解和生成能力。

