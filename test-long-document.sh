# 重复内容以达到超长文本（超过100,000字符）
long_content = content * 60

with open('/tmp/test_long.txt', 'w', encoding='utf-8') as f:
    f.write(long_content)

print(f"   ✅ 文件已创建: /tmp/test_long.txt")
print(f"   📊 字符数: {len(long_content):,} 字符")
print(f"   ⚠️  超过限制: {len(long_content) - 100000:,} 字符（将被截断）")
EOF

echo ""

# 2. 上传文件
echo "📤 步骤 2: 上传文件到 Supabase..."
UPLOAD_RESULT=$(curl -s -X POST http://localhost:3000/api/upload -F "file=@/tmp/test_long.txt")
echo "   $UPLOAD_RESULT"

# 提取文件名
FILENAME=$(echo $UPLOAD_RESULT | grep -oP '(?<=documents/)[^"]+')
echo "   ✅ 上传成功，文件名: $FILENAME"
echo ""

# 3. 等待避免速率限制
echo "⏳ 步骤 3: 等待 10 秒以避免速率限制..."
sleep 10
echo "   ✅ 等待完成"
echo ""

# 4. 调用摘要API
echo "🤖 步骤 4: 调用 AI 摘要 API（可能需要等待10-30秒）..."
SUMMARY_RESULT=$(curl -s -X POST http://localhost:3000/api/summary \
  -H "Content-Type: application/json" \
  -d "{\"filename\": \"$FILENAME\"}")

echo ""
echo "================================================================================  "
echo "📊 测试结果"
echo "================================================================================  "
echo "$SUMMARY_RESULT" | python3 -m json.tool 2>/dev/null || echo "$SUMMARY_RESULT"
echo ""

# 5. 检查结果
if echo "$SUMMARY_RESULT" | grep -q "isTruncated.*true"; then
    echo "✅ 超长文本处理测试通过："
    echo "   - 系统正确识别并截断了超过 100,000 字符的文本"
    echo "   - 生成了基于截断文本的摘要"
    echo "   - 返回了警告信息"
elif echo "$SUMMARY_RESULT" | grep -q "summary"; then
    echo "✅ 摘要生成成功（文本未超长或已从缓存返回）"
elif echo "$SUMMARY_RESULT" | grep -q "RATE_LIMIT"; then
    echo "⚠️  遇到API速率限制，请稍后重试"
    echo "   解决方案："
    echo "   - 等待 1-2 分钟后再次运行此脚本"
    echo "   - 检查智谱AI账户的免费额度"
else
    echo "❌ 测试失败，请检查错误信息"
fi

